package entity.base;

public interface HasCircleBounds {

	/**
	 * Getter for the X axis center position of the entity.
	 *
	 * @return Center position of the entity in the X axis.
	 */
	int getPositionCX();

	/**
	 * Getter for the Y axis center position of the entity.
	 *
	 * @return Center position of the entity in the Y axis.
	 */
	int getPositionCY();

	/**
	 * Getter for the width of the image associated to the entity.
	 *
	 * @return Radius of the entity.
	 */
	int getRadius();

}
